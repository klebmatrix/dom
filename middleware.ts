// middleware.ts
import { createMiddlewareClient } from '@supabase/auth-helpers-nextjs';
import { NextResponse } from 'next/server';
import type { NextRequest } from 'next/server';

export async function middleware(req: NextRequest) {
  const res = NextResponse.next();
  const supabase = createMiddlewareClient({ req, res });

  // A chamada a getSession() JÁ DEVE SER AWAITED.
  // O erro indica que algo interno na biblioteca pode estar a tentar aceder a cookies de forma síncrona.
  // Mas vamos garantir que a nossa chamada principal está correta.
  await supabase.auth.getSession(); // Esta linha já está com await

  return res;
}

export const config = {
  matcher: [
    /*
     * Match all request paths except for the ones starting with:
     * - _next/static (static files)
     * - _next/image (image optimization files)
     * - favicon.ico (favicon file)
     * Feel free to modify this pattern to include more paths.
     */
    '/((?!_next/static|_next/image|favicon.ico).*)',
  ],
};
